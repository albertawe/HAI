    private void tableviewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableviewMouseClicked
        try {
            con = Connect.ConnectDB();
            int row = tableview.getSelectedRow();
            String table_click = tableview.getModel().getValueAt(row, 0).toString();
            sql = "select * from tbpsikotest where idPsikotest = '" + table_click + "'";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {

                String colidPsikotest = rs.getString("idPsikotest");
                tfidPsikotest.setText(colidPsikotest);
                String colidPelamar = rs.getString("idPelamar");
                tfidPelamar.setText(colidPelamar);
                String colidPsikolog = rs.getString("idPsikolog");
                tfidPsikolog.setText(colidPsikolog);
                String colhasil = rs.getString("hasil");
                tfhasil.setText(colhasil);
                String coldok = rs.getString("dokPsikotest");
                lbfilename.setText(coldok);
                
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_tableviewMouseClicked

