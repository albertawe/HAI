    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        stridPsikotest = tfidPsikotest.getText();
       

        try {
            con = Connect.ConnectDB();
            if (stridPsikotest.equals("")) {
                JOptionPane.showMessageDialog(this, "Please enter id psikotest to delete", "Error", JOptionPane.ERROR_MESSAGE);
                return;

            }
            

            jlhbaris = 0;
            Statement stmt;
            stmt = con.createStatement();
            sql = "Select * from tbpsikotest where idPsikotest= '" + stridPsikotest + "'";
            rs = stmt.executeQuery(sql);
            if (rs.next()) {
                jlhbaris++;
            }

            if (jlhbaris == 1) {
                sql = "delete from tbpsikotest where idPsikotest='" + stridPsikotest + "'";

                pst = con.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(this, "Successfully delete data", "Psikotest", JOptionPane.INFORMATION_MESSAGE);
                reset();
                Get_Data();
            }

        } catch (HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
           
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

