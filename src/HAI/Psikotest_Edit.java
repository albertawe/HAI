    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        stridPsikotest = tfidPsikotest.getText();
        stridPelamar = tfidPelamar.getText();
        stridPsikolog = tfidPsikolog.getText();
        strhasil = tfhasil.getText();
        strfile = lbfilename.getText();

        try {
            con = Connect.ConnectDB();
            if (stridPsikotest.equals("")) {
                JOptionPane.showMessageDialog(this, "Please enter idpsikotest", "Error", JOptionPane.ERROR_MESSAGE);
                return;

            }
            if (stridPelamar.equals("")) {
                JOptionPane.showMessageDialog(this, "Please enter idpelamar", "Error", JOptionPane.ERROR_MESSAGE);
                return;

            }
            if (stridPsikolog.equals("")) {
                JOptionPane.showMessageDialog(this, "Please enter idpsikolog", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (strhasil.equals("")) {
                JOptionPane.showMessageDialog(this, "Please enter hasil", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (strfile.equals("")) {
                JOptionPane.showMessageDialog(this, "Please select file", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            jlhbaris = 0;
            Statement stmt;
            stmt = con.createStatement();
            sql = "Select * from tbpsikotest where idPsikotest= '" + stridPsikotest + "'";
            rs = stmt.executeQuery(sql);
            if (rs.next()) {
                
                jlhbaris++;
                return;
            }

            if (jlhbaris == 1) {
                sql = "update tbdatapelamar set idPelamar='" + stridPelamar + "', idPsikolog='" + stridPsikolog + "', hasil='" + strhasil + "', '"
                        + lbfilename + "' where idPsikotest='" + stridPsikotest + "'";

                pst = con.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(this, "Successfully update data", "Psikotest", JOptionPane.INFORMATION_MESSAGE);
                reset();
            }

        } catch (HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

